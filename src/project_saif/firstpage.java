    public void calculate(){
        if(opt==""){
            fnum = Double.parseDouble(display.getText());
           // display.setText("");
        }
        
        else
        {
            String answer;
            
            snum = Double.parseDouble(display.getText());
            if(opt == "+")
            {
                result = fnum + snum;
                answer = String.format("%.0f", result);
                fnum=result;
                display.setText(answer);
            }

                else if(opt == "-")
            {
                result = fnum - snum;
                answer = String.format("%.0f", result);
                fnum=result;
                display.setText(answer);
            }

                else if(opt == "*")
            {
                result = fnum * snum;
                answer = String.format("%.0f", result);
                fnum=result;
                display.setText(answer);
            }

                else if(opt == "/")
            {
                result = fnum / snum;
                answer = String.format("%.0f", result);
                fnum=result;
                display.setText(answer);
            }
        }
        //display.setText("");
        flag=1;
    }
